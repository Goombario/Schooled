struct Tile
{
	char character;
	int colourCode;
	bool isPassable;
};

Tile tileIndex[] = {
	{ ' ', 7, true },	// (0) MAP_FLOOR
	{ '_', FOREGROUND_BLUE, false },	// (1) MAP_WALL_TOP
	{ 'D', 7, true },	// (2) MAP_DOOR
	{ '|', 7, false }	// (3) MAP_WALL_SIDE
};

void displayMap();
void drawTile(int x, int y);
void interactable();
bool isPassable(int mapX, int mapY);


int main()
{
	console.SetTitle("Schooled V0.1");

	// Initialization
	// Initialize the player's on-screen location
	int nPlayerX = 2, nPlayerY = 19;
	int nDeltaX, nDeltaY;

	// Main program loop
	while (true)
	{
		// Wipe console clear
		console.Clear();

		// Output phase
		displayMap();
		console.Position(nPlayerX, nPlayerY);
		console << '8';

		// Input phase
		KEYPRESS sKeyPress = console.WaitForKeypress();

		// Processing phase
		switch (sKeyPress.eCode)
		{
			// move down
		case CONSOLE_KEY_DOWN:
			nDeltaX = 0;
			nDeltaY = 1;
			break;

			// move left
		case CONSOLE_KEY_LEFT:
			nDeltaX = -1;
			nDeltaY = 0;
			break;

			// move right
		case CONSOLE_KEY_RIGHT:
			nDeltaX = 1;
			nDeltaY = 0;
			break;

			// move up
		case CONSOLE_KEY_UP:
			nDeltaX = 0;
			nDeltaY = -1;
			break;

			// quit
		case CONSOLE_KEY_ESCAPE:
			return 0;

			// Ignore any other key
		default:
			break;
		}

		// Check if the player can move in specified direction
		if (isPassable(nPlayerX + nDeltaX, nPlayerY + nDeltaY))
		{
			// If allowed, move in specified direction
			nPlayerX += nDeltaX;
			nPlayerY += nDeltaY;
		}

		nDeltaX = 0;
		nDeltaY = 0;

	}
	return 0;
}
void displayMap(){
	for (int a = 0; a < schooled::MAP_HEIGHT; a++){
		console.Position(0,a);
		for (int b = 0; b < schooled::MAP_WIDTH; b++){
			drawTile(b, a);
		}
	}
}

void drawTile(int x, int y)
{
	HANDLE hConsole = GetStdHandle(STD_OUTPUT_HANDLE);  // Get handle to standard output
	console.Position(x, y);
	int tile = roomOneArray[y][x];
	//console.Color(tileIndex[tile].colourCode);
	if (tile == 0)
		SetConsoleTextAttribute(hConsole, tileIndex[tile].colourCode);
	console << tileIndex[tile].character;
}

void interactable(){
	console.Position(18, 16);
	console << 'X';
}
bool isPassable(int mapX, int mapY){
	if (mapX < 0 || mapX >= schooled::MAP_WIDTH || mapY < 0 || mapY >= schooled::MAP_HEIGHT)
		return false;

	int tileValue = roomOneArray[mapY][mapX];
	if (tileValue == 0)
		return true;
	else if (tileValue == 2)
		return true;
	else
		return false;
}
